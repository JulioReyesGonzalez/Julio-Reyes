# Definir la variable color correctamente (debe ir entre comillas porque es una cadena de texto)
color = "rojo"

# Estructura correcta del condicional if-else
if color == "rojo":  # Se usa "if" primero con la comparación correcta
    print("El color es rojo.")  # La función print debe ir en minúsculas y con paréntesis
else:  # "else" no lleva condición, simplemente se ejecuta si el "if" es falso
    print("El color no es rojo.")

