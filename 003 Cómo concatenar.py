# Concatenar dos strings en una variable
mensaje = "Hola" + " mundo"
print(mensaje)

# Concatenar dos variables con strings en una tercera variable
texto1 = "Python"
texto2 = " es facil"
resultado = texto1 + texto2
print(resultado)

# Concatenar una variable con un string directamente en un print
nombre = "Julio"
print(nombre + " esta aprendiendo Python.")

# Concatenar nombre y apellidos con espacios
nombre = "Julio"
apellido1 = "Reyes"
apellido2 = "Gonzalez"
nombre_completo = nombre + " " + apellido1 + " " + apellido2
print(nombre_completo)

# Concatenar dos números como strings
num1 = "123"
num2 = "456"
num_concatenado = num1 + num2
print(num_concatenado)

